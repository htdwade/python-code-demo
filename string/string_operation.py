name = "ada lovelace"
# title()将每个单词的首字母改为大写
print(name.title())

# upper()将所有字母改成大写
print(name.upper())

# lower()将所有字母改成小写
print(name.lower())


message = ' python '
# 删除开头的空白
print(message.lstrip())

# 删除末尾的空白
print(message.rstrip())

# 删除两端的空白
print(message.strip())


age = 23
# str()将非字符窜转为字符串
message = "Happy " + str(age) + "rd Birthday!"
print(message)
